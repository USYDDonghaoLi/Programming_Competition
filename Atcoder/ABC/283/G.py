from types import GeneratorType
def bootstrap(f, stack=[]):
    def wrappedfunc(*args, **kwargs):
        if stack:
            return f(*args, **kwargs)
        else:
            to = f(*args, **kwargs)
            while True:
                if type(to) is GeneratorType:
                    stack.append(to)
                    to = next(to)
                else:
                    stack.pop()
                    if not stack:
                        break
                    to = stack[-1].send(to)
            return to
    return wrappedfunc

class LinearBase:

    def __init__(self, nums) -> None:
        self.bases = []
        self.n = len(nums)
        for num in nums:
            tot = len(self.bases)
            for i in range(tot):
                num = min(num, num ^ self.bases[i])
            if num:
                for i in range(tot):
                    self.bases[i] = min(self.bases[i], self.bases[i] ^ num)
                self.bases.append(num)
        self.bases.sort()
    
