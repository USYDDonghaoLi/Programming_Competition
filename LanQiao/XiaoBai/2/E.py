def solve(testcase):
    a, n = MI()
    if a == 1:
        print(0)
    else:
        g = 1
        for i in range(1, a):
            g = g * i // gcd(g, i)
            if g > n:
                print(0)
                return
        if g % a == 0:
            print(0)
        else:
            print(n // g - n // ((g * a) // gcd(g, a)))            

for testcase in range(II()):
    solve(testcase)